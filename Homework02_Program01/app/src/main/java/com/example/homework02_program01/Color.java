package com.example.homework02_program01;

public class Color
{

    private int red;
    private int green;
    private int blue;

    public Color()
    {

    }

    public Color(int r, int g, int b)
    {
        red = r;
        green = g;
        blue = b;
    }

    public int getRed() {
        return red;
    }

    public void setRed(int r) {
        red = r;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int g) {
        green = g;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int b) {
        blue = b;
    }



}
